package com.mercadolibre.mutants.model;

import java.util.List;

import javax.validation.constraints.NotNull;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "mutants")
public class MutantModel {

	private String id;
	@NotNull
	private List<String> adn;
	private Boolean isMutant;
	
	public MutantModel() {
		// TODO Auto-generated constructor stub
	}

	public MutantModel(List<String> adn, Boolean isMutant) {		
		this.adn = adn;
		this.isMutant = isMutant;
	}

	@DynamoDBHashKey
	@DynamoDBAutoGeneratedKey
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	//@DynamoDBRangeKey	
	@DynamoDBAttribute
	public List<String> getAdn() {
		return adn;
	}

	public void setAdn(List<String> adn) {
		this.adn = adn;
	}

	@DynamoDBAttribute	
	public Boolean getIsMutant() {
		return isMutant;
	}

	public void setIsMutant(Boolean isMutant) {
		this.isMutant = isMutant;
	}

}
